static unsigned int lcm_compare_id(void)
{
	unsigned int id = 0;
	unsigned char buffer[3];
    unsigned int data_array[16];
	
	SET_RESET_PIN(1);  //NOTE:should reset LCM firstly
    MDELAY(10);
	SET_RESET_PIN(0);
    MDELAY(10);
	SET_RESET_PIN(1);
    MDELAY(10);	
   
    //*************Enable CMD2 Page1  *******************//
    data_array[0]=0x00063902;
    data_array[1]=0x52AA55F0;
    data_array[2]=0x00000108;
    dsi_set_cmdq(data_array, 3, 1);
    MDELAY(10); 
    
    data_array[0] = 0x00033700; // read id return two byte,version and id
    dsi_set_cmdq(data_array, 1, 1);
    MDELAY(10); 

	read_reg_v2(0xc5, buffer, 3);
    id  |= buffer[0];
    id  <<= 8;
    id  |=buffer[1];
  
#ifdef BUILD_LK
	//printf("zhuoshineng LK RM68191 debug: RM68191 lcd_id = %d\n",lcd_id);
	printf("zhuoshineng LK RM68191 debug: RM68191 id = %x\n",id);

#else
	//printk("zhuoshineng kernel RM68191  debug: RM68191 lcd_id= %d\n", lcd_id);
	printk("zhuoshineng kernel RM68191  debug: RM68191 id = %x\n", id);
#endif

  
    return (LCM_ID == id)?1:0;
}


