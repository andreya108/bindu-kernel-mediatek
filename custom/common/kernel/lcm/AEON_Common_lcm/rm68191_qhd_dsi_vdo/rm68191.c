static void lcm_get_params(LCM_PARAMS *params)
{
		memset(params, 0, sizeof(LCM_PARAMS));
	
		params->type   = LCM_TYPE_DSI;
		params->width  = FRAME_WIDTH;
		params->height = FRAME_HEIGHT;
    params->dbi.te_mode	 = LCM_DBI_TE_MODE_VSYNC_ONLY;
    params->dbi.te_edge_polarity= LCM_POLARITY_RISING;
    params->dsi.mode   = BURST_VDO_MODE;
    params->dsi.LANE_NUM	= LCM_TWO_LANE;

		//The following defined the fomat for data coming from LCD engine.
		params->dsi.data_format.color_order = LCM_COLOR_ORDER_RGB;
		params->dsi.data_format.trans_seq   = LCM_DSI_TRANS_SEQ_MSB_FIRST;
		params->dsi.data_format.padding     = LCM_DSI_PADDING_ON_LSB;
		params->dsi.data_format.format      = LCM_DSI_FORMAT_RGB888;

		// Video mode setting		
		params->dsi.intermediat_buffer_num = 2;

		params->dsi.PS=LCM_PACKED_PS_24BIT_RGB888;

    params->dsi.word_count=480*3;	//DSI CMD mode need set these two bellow params, different to 6577

    params->dsi.vertical_sync_active				= 4;
    params->dsi.vertical_backporch					= 16;
    params->dsi.vertical_frontporch					= 16;
		params->dsi.vertical_active_line				= FRAME_HEIGHT; 

    params->dsi.horizontal_sync_active				= 4;
    params->dsi.horizontal_backporch				= 20;//50
    params->dsi.horizontal_frontporch				= 20;//50
    params->dsi.horizontal_blanking_pixel				= 20;
		params->dsi.horizontal_active_pixel				= FRAME_WIDTH;


    params->dsi.pll_div1=1;		// div1=0,1,2,3;div1_real=1,2,4,4
    params->dsi.pll_div2=1;		// div2=0,1,2,3;div2_real=1,2,4,4
    params->dsi.fbk_sel=1;		 // fbk_sel=0,1,2,3;fbk_sel_real=1,2,4,4
    params->dsi.fbk_div =32;	           //25	// fref=26MHz, fvco=fref*(fbk_div+1)*2/(div1_real*div2_real)		

}


static void lcm_init(void)
{
    SET_RESET_PIN(1);
    MDELAY(50);
    SET_RESET_PIN(0);
    MDELAY(50);
    SET_RESET_PIN(1);
    MDELAY(200);

	push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);
}
static void lcm_suspend(void)
{
	     //需先关显示，再reset，否则会干扰psensor等外设
	     push_table(lcm_deep_sleep_mode_in_setting, sizeof(lcm_deep_sleep_mode_in_setting) / sizeof(struct LCM_setting_table), 1);
    SET_RESET_PIN(1);
        MDELAY(10);
    SET_RESET_PIN(0);
        MDELAY(10);
    SET_RESET_PIN(1);
        MDELAY(200);
}
static void lcm_resume(void)
{
    lcm_init();    
}
         

